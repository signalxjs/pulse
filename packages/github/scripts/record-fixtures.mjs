// Refresh the committed fixtures from the live GitHub API, then regenerate
// the static-import module the adapter reads (src/fixtures-data.js).
//
//   GITHUB_TOKEN=$(gh auth token) node packages/github/scripts/record-fixtures.mjs
//
// To only regenerate fixtures-data.js (after hand-editing fixture JSONs,
// e.g. the lumen/lumen demo dataset — the recorder never touches those):
//
//   node packages/github/scripts/record-fixtures.mjs --regen-only
//
// Public signalxjs data is recorded as-is (already-mapped client shapes,
// which is exactly what the fixtures adapter serves back). The viewer and
// org membership are SYNTHETIC — fixtures must not embed anyone's identity.
import { mkdirSync, writeFileSync, readdirSync } from 'node:fs';
import { join, dirname, relative, sep } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createLiveClient } from '../src/index.js';

const dir = join(dirname(fileURLToPath(import.meta.url)), '..', 'fixtures');
const regenOnly = process.argv.includes('--regen-only');

const write = (rel, data) => {
    const path = join(dir, rel);
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, JSON.stringify(data, null, 2) + '\n');
    console.log('wrote', rel);
};

/**
 * Regenerate src/fixtures-data.js: one static JSON import per recording,
 * collected into a map keyed by fixtures-relative path. Static imports keep
 * the adapter fs-free (Node 24 and Vite both support import attributes).
 */
function regenFixturesData() {
    /** @type {string[]} */
    const files = [];
    (function walk(/** @type {string} */ d) {
        for (const entry of readdirSync(d, { withFileTypes: true })) {
            const path = join(d, entry.name);
            if (entry.isDirectory()) walk(path);
            else if (entry.name.endsWith('.json')) files.push(relative(dir, path).split(sep).join('/'));
        }
    })(dir);
    files.sort();
    const lines = [
        '/* generated by scripts/record-fixtures.mjs — do not edit */',
        ...files.map((f, i) => `import f${i} from '../fixtures/${f}' with { type: 'json' };`),
        '',
        '/** @type {Record<string, any>} */',
        'export const FIXTURES = {',
        ...files.map((f, i) => `    '${f}': f${i},`),
        '};',
        ''
    ];
    writeFileSync(join(dir, '..', 'src', 'fixtures-data.js'), lines.join('\n'));
    console.log(`wrote src/fixtures-data.js (${files.length} fixtures)`);
}

if (!regenOnly) {
    const token = process.env.GITHUB_TOKEN;
    if (!token) {
        console.error('GITHUB_TOKEN required (e.g. GITHUB_TOKEN=$(gh auth token)), or pass --regen-only');
        process.exit(1);
    }
    const gh = createLiveClient({ token });

    // Synthetic identity — deterministic, nobody's real account.
    write('viewer.json', {
        login: 'pulse-dev',
        name: 'Pulse Developer',
        avatarUrl: 'https://avatars.githubusercontent.com/u/0?v=4'
    });
    write('viewer-orgs.json', [
        { login: 'signalxjs', avatarUrl: 'https://avatars.githubusercontent.com/u/230127961?v=4', description: 'SignalX — fine-grained reactive UI' }
    ]);

    const signalxRepos = await gh.ownerRepos('signalxjs');
    write('viewer-repos.json', signalxRepos);
    write(join('owner-repos', 'signalxjs.json'), signalxRepos);
    write(join('repo', 'signalxjs', 'core.json'), await gh.repo('signalxjs', 'core'));
    write(join('repo', 'signalxjs', 'pulse.json'), await gh.repo('signalxjs', 'pulse'));
}

regenFixturesData();
console.log('done');
